export default function productsReducer(state=initialState, action) {
const {type, payload}=action 
   switch (type) {
       case 'GET_ALL_RODUCTS_REQUEST':
         return {
           ...state, loading:true
         }
       break
       case 'GET_ALL_RODUCTS_RESPONSE':  
           return{
          ...state, productsList: payload.productList,
          loading: false
        }
       break;
       case 'GET_ALL_RODUCTS_ERROR':
         return {
           ...state,  loading:true
         }
         break;
       case 'GET_ALL_CATEGORY_REQUEST' :
         return {
           ...state,  loading:true
         }
         break;

        case 'GET_ALL_CATEGORY_RESPONSE':
          return {
            ...state, allCategoryList: payload.categorieslist,
            loading:false
          } 
         break;
        case 'GET_ALL_CATEGORY_ERROR' :
         return {
            ...state, loading:true
          }
         break; 
        case 'GET_ALL_PRODUCT_CART_REQUEST': 
         return{
            ...state, loading:true
          }
         break;
        case 'GET_ALL_PRODUCT_CART_RESPONSE': 
         return{
           ...state, allProductCart: payload.productList,
           loading:false
        }
         break;
        case 'GET_ALL_PRODUCT_CART_ERROR': 
         return{
           ...state, loading:true
        } 
         break; 
        case 'GET_ATTRIBUT_TYPE_PRODUCT_REQUEST': 
         return{
           ...state, loading:true
        } 
         break; 
         case 'GET_ATTRIBUT_TYPE_PRODUCT_RESPONSE': 
         return{
           ...state, attributTypeProduct:payload, loading:false
        } 
         break; 
         case 'GET_ATTRIBUT_TYPE_PRODUCT_ERROR': 
         return{
           ...state, loading:true
        } 
         break; 
         case 'GET_PRODUCT_UPLOADED_IMAGES_AND_VIDEOS_REQUEST':
           return {
            ...state,loading:true
           } 
           break;
          case 'GET_PRODUCT_UPLOADED_IMAGES_AND_VIDEOS_RESPONSE':
          return {
           ...state,  productVideoAndImage:payload.expertAttachments, loading:false
          } 
          break;
          case 'GET_PRODUCT_UPLOADED_IMAGES_AND_VIDEOS_ERROR':
            return {
             ...state, loading:true
            } 
            break;
          
            // case 'SET_ATTRIBUTE_TYPE_PRODUCT_REQUEST':
            // return {
            //   ...state,  loading:true
            // }
            // break;
            case 'SET_ATTRIBUTE_TYPE_PRODUCT_RESPONSE':
            return{
              ...state,  loading:false,  attributProductResponse:payload
            }
          //   break;
          // case 'SET_ATTRIBUTE_TYPE_PRODUCT_ERROR':
          //   return {
          //     ...state,  loading:true
          //   }
            break;
       default:
           return{
               ...state
           }
        break;
   }
}
